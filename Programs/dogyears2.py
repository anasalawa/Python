# Converting dog years to human years

name = input("What is your name? ")
print ("Hello,", name + ".")

# Human age
H_age = float(input("What is your age? "))

# Dog age
D_age = H_age * 7

print ("Spectacular,", name + ", you don't seem as though you are", H_age, "years old! You are", D_age, "in dog years!")